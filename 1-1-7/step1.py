#list
new_list = ["dog", "cat", "mouse", "bird", "monkey"]
print(new_list)

#print specific item
print(new_list[2])

#change specific item
new_list[2]="dog"
print(new_list)

#loops
for item in new_list:
    print(item)

#appending
new_list.append("lion")
print(new_list)

#popping
new_list.pop()
print(new_list)
